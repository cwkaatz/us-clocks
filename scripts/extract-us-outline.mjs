// Extract US outline (contiguous 48 + Alaska, no Hawaii) from us-atlas
// states-albers-10m TopoJSON and write a flat TS file the bundle imports
// at build time. Run with:
//   node scripts/extract-us-outline.mjs
// Re-run after changing the filter rules.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { mesh } from "topojson-client";

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, "..");
const dataPath = path.resolve(root, "node_modules/us-atlas/states-albers-10m.json");
const outPath = path.resolve(root, "src/us-outline.ts");

const raw = JSON.parse(fs.readFileSync(dataPath, "utf-8"));

// Filter out Hawaii (FIPS id "15"). Keep Alaska (FIPS id "02") and everything else.
const filtered = {
  ...raw,
  objects: {
    ...raw.objects,
    states: {
      ...raw.objects.states,
      geometries: raw.objects.states.geometries.filter((g) => String(g.id) !== "15"),
    },
  },
};

// topojson.mesh with the predicate (a, b) => a === b returns only exterior arcs
// (where both sides of the arc reference the same geometry — i.e., no
// neighbouring state on the other side). The result is the merged outer
// boundary of the contiguous 48 + AK, with no internal state borders.
const outline = mesh(filtered, filtered.objects.states, (a, b) => a === b);

const polylines = outline.coordinates.map((line) =>
  line.map(([x, y]) => [Math.round(x), Math.round(y)]),
);

let minX = Infinity;
let maxX = -Infinity;
let minY = Infinity;
let maxY = -Infinity;
for (const line of polylines) {
  for (const [x, y] of line) {
    if (x < minX) minX = x;
    if (x > maxX) maxX = x;
    if (y < minY) minY = y;
    if (y > maxY) maxY = y;
  }
}

const totalPoints = polylines.reduce((acc, line) => acc + line.length, 0);

const ts = `// AUTO-GENERATED by scripts/extract-us-outline.mjs. Do not edit by hand.
// Source: us-atlas states-albers-10m.json (public domain Natural Earth via D3).
// Pre-projected to Albers USA, Hawaii excluded.
// ${polylines.length} polylines, ${totalPoints} total points.
export const US_OUTLINE_POLYLINES: ReadonlyArray<ReadonlyArray<readonly [number, number]>> = ${JSON.stringify(polylines)} as const;

// Bounding box of the source data, used by the renderer to fit-and-center.
export const US_OUTLINE_BOUNDS = { minX: ${minX}, maxX: ${maxX}, minY: ${minY}, maxY: ${maxY} } as const;
`;

fs.writeFileSync(outPath, ts);
console.log(`Wrote ${outPath}: ${polylines.length} polylines, ${totalPoints} points`);
